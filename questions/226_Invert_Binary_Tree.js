/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {TreeNode}
 */
var invertTree = function (root) {
    let node;
    if (root !== null) {
        node = new TreeNode(root.val);
        node.left = invertTree(root.right);
        node.right = invertTree(root.left);
    } else {
        return null;
    }
    return node;
};